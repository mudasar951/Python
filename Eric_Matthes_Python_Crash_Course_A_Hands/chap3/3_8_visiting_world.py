places_list = ['Iraq', 'Germany', 'Pakistan']

#original list
print(places_list)
#sorted list
print(sorted(places_list))
#original list
print(places_list)

#list in reverse order
print(sorted(places_list, reverse=True))
#original list
print(places_list)
#reversing list
places_list.reverse()
print(places_list)

places_list.reverse()
print(places_list)

places_list.sort()
print(places_list)
places_list.sort(reverse=True)
print(places_list)